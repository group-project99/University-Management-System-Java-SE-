    /**
     * search student,course,register details and payment details
     *
     * @param evt
     */
    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        c = new Course();
        r = new Register();
        s = new Student();

        if (!search.getText().equals("")) {

            //check student register payment
            if (rs.checkStudentRegistrationPayment(search.getText())) {

                idLable.setText("");

                try {

                    //search student,course,register details
                    Student s = ps.search(search.getText());

                    studentName.setText(s.getsName());
                    course.setText(s.getCourse().getaCourseName());
                    paymentPlan.setText(s.getRegister().getPaymentPlan());
                    courseFee.setText(String.valueOf(s.getRegister().getCourseFee()));

                    sid = s.getSid();
                    cid = s.getCourse().getCid();
                    email = s.getEmail();
                    cFee = (String.valueOf(s.getRegister().getCourseFee()));

                    if (ps.checkPayment(search.getText())) {
                        //search payment details                     
                        Payment p = ps.getPaidAmount(search.getText());

                        paidAmount.setText(String.valueOf(p.getPaidAmount()));
                        balance.setText(String.valueOf(p.getBalance()));
                    } else {

                        paidAmount.setText("00.0");
                        balance.setText("00.0");
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }

                c.setaCourseName(course.getText());
                r.setPaymentPlan(paymentPlan.getText());

                s.setCourse(c);
                s.setRegister(r);

                //view instalment
                instalment.setText(String.valueOf(ps.viewInstalment(s)));

            } else {
                idLable.setText("Not a Registed Student ID");
                clear();
            }

        } else {
            search.setText("");
            idLable.setText("");
            clear();
        }
    }//GEN-LAST:event_searchKeyReleased

