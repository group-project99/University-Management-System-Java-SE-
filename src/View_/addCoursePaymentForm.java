    /**
     * instalmentB action
     *
     * @param evt
     */
    private void instalmentBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_instalmentBActionPerformed
        p = new Payment();
        r = new Register();
        s = new Student();

        if (!studentName.getText().equals("") && instalmentB.isSelected()) {

            if (paidAmount.getText().equals(courseFee.getText())) {

                JOptionPane.showMessageDialog(null, studentName.getText() + " is complited course payments");
                instalmentB.setSelected(false);

            } else if (!paidAmount.equals("00.0")) {

                p.setInstalment(Float.valueOf(instalment.getText()));
                p.setPaidAmount(Float.valueOf(paidAmount.getText()));

                //view paid amount
                paidAmount.setText(String.valueOf(ps.viewPaidAmount(p)));

            } else {
                paidAmount.setText(instalment.getText());
            }

            r.setCourseFee(Float.valueOf(courseFee.getText()));
            p.setPaidAmount(Float.valueOf(paidAmount.getText()));

            s.setRegister(r);
            s.setPayment(p);
            //view balance
            balance.setText(String.valueOf(ps.viewBalance(s)));

        } else if (!studentName.equals("") && !instalmentB.isSelected()) {

            //check payment
            if (ps.checkPayment(search.getText())) {

                //search payment details                                 
                Payment p = ps.getPaidAmount(search.getText());

                paidAmount.setText(String.valueOf(p.getPaidAmount()));
                balance.setText(String.valueOf(p.getBalance()));

            } else {

                paidAmount.setText("00.0");
                balance.setText("00.0");

            }

        } else {
            JOptionPane.showMessageDialog(null, "Search a student");
            instalmentB.setSelected(false);
        }
    }//GEN-LAST:event_instalmentBActionPerformed

    /**
     * add course payment
     *
     * @param evt
     */
    private void addBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBActionPerformed
        p = new Payment();

        if (studentName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Search a student");
        } else if (!instalmentB.isSelected()) {
            JOptionPane.showMessageDialog(null, "Need a course payment");
        } else {
            p.setSid(sid);
            p.setCid(cid);
            p.setInstalment(Float.valueOf(instalment.getText()));
            p.setPaidAmount(Float.valueOf(paidAmount.getText()));
            p.setBalance(Float.valueOf(balance.getText()));
            p.setDate(date.getText());
            p.setTime(time.getText());

            ps.addCoursePayment(p);

            // send payment email`    
            try {
                JavaMailUtil.sendPaymentMail(email, cFee, instalment.getText(), paidAmount.getText(), balance.getText());
            } catch (Exception ex) {
                Logger.getLogger(addNewStudentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            genarateB.setVisible(true);
        }
    }//GEN-LAST:event_addBActionPerformed

