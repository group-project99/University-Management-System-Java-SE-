    /**
     * add new student
     *
     * @param evt
     */
    private void addbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbActionPerformed
        s = new Student();
        ss.nextId(s);
        nextid = s.getSid() + 1;
        System.out.println(nextid);

        //Generate a Barcode
        try {

            Linear barcode = new Linear();
            barcode.setType(Linear.CODE128B);
            barcode.setData(String.valueOf(s.getSid()));
            barcode.setI(11.0f);
            barcode.renderBarcode(barcodepath + nextid + ".png");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        try {
            filename = barcodepath + nextid + ".png";
            File img = new File(filename);
            FileInputStream fis = new FileInputStream(img);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];

            for (int readNum; (readNum = fis.read(buf)) != -1;) {

                bos.write(buf, 0, readNum);
            }
            barcodeimage = bos.toByteArray();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }

        if (male.isSelected()) {
            gender = "Male";
        } else if (female.isSelected()) {
            gender = "Female";
        } else {
            gender = "";
        }

        if (firstName.getText().equals("") || lastName.getText().equals("") || age.getText().equals("")) {
            //JOptionPane.showMessageDialog(null, "One or more fields are empty");
            oneOrMoreFieldEmpty_new var = new oneOrMoreFieldEmpty_new();
            var.setVisible(true);
        } else if (nic.getText().equals("") || gender.equals("") || telephone.getText().equals("") || email.getText().equals("")) {
            //JOptionPane.showMessageDialog(null, "One or more fields are empty");
            oneOrMoreFieldEmpty_new var = new oneOrMoreFieldEmpty_new();
            var.setVisible(true);
        } else if (!email.getText().matches("^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$")) {
            JOptionPane.showMessageDialog(null, "Invalid Email Address", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (imagePath == null) {
            JOptionPane.showMessageDialog(null, "No image");
        } else if (address.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No address");
        } else if (branch.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Plz select a branch");
        } else if (trusteeName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No trustee name");
        } else if (course.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Plz select a course");
        } else if (academicYear.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Plz select a academic year");
        } else {
            s.setsName(firstName.getText() + " " + lastName.getText());
            s.setAge(age.getText());
            s.setNic(nic.getText());
            s.setGender(gender);
            s.setTelephone(telephone.getText());
            s.setEmail(email.getText());
            s.setImagePath(imagePath);
            s.setAddress(address.getText());
            s.setTName(trusteeName.getText());
            s.setBranch(branch.getSelectedItem().toString());
            s.setaYear(academicYear.getSelectedItem().toString());
            s.setCid(course.getSelectedIndex());
            s.setBarcode(barcodeimage);

            ss.save(s);

            //send fee structure email
            try {
                JavaMailUtil.sendFeeStructureMail(email.getText(), course.getSelectedItem().toString());
            } catch (Exception ex) {
                Logger.getLogger(addNewStudentForm.class.getName()).log(Level.SEVERE, null, ex);
            }

            clear();
        }
    }//GEN-LAST:event_addbActionPerformed

