    public ImageIcon resizePic(String picPath) {
        ImageIcon myImg = new ImageIcon(picPath);
        Image img = myImg.getImage().getScaledInstance(picture.getWidth(), picture.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon myPicture = new ImageIcon(img);
        return myPicture;
    }

    /**
     * browseB action
     *
     * @param evt
     */
    private void browseBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseBActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter fnef = new FileNameExtensionFilter("*.image", "jpg", "png", "gif");
        fc.addChoosableFileFilter(fnef);

        int fileState = fc.showSaveDialog(null);

        if (fileState == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fc.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            imagePath = path;

            picture.setIcon(resizePic(path));
        } else if (fileState == JFileChooser.CANCEL_OPTION) {
            System.out.println("no image selected");
        }
    }//GEN-LAST:event_browseBActionPerformed

